#include <asm.h>

char	**delete_grid(char **grid)
{
	size_t	i;

	i = 0;
	while (grid[i])
	{
		ft_memdel((void*)&grid[i]);
		i++;
	}
	ft_memdel((void*)&grid);
	return (NULL);
}
